CREATE TABLE BRANCH(
BRANCH_NAME VARCHAR2(20), BRANCH_CITY VARCHAR2(20), ASSETS NUMBER(10,2),PRIMARY KEY(BRANCH_NAME));

CREATE TABLE ACCOUNTS(
ACCNO NUMBER(20), BRANCH_NAME VARCHAR2(20), BALANCE NUMBER(10,2),PRIMARY KEY(ACCNO),
FOREIGN KEY(BRANCH_NAME) REFERENCES BRANCH (BRANCH_NAME) ON DELETE CASCADE);

CREATE TABLE DEPOSITOR(
CUSTOMER_NAME VARCHAR2(25), ACCNO NUMBER(20),
FOREIGN KEY(CUSTOMER_NAME) REFERENCES CUSTOMER (CUSTOMER_NAME) ON DELETE CASCADE,
FOREIGN KEY(ACCNO) REFERENCES ACCOUNTS (ACCNO) ON DELETE CASCADE);

CREATE TABLE CUSTOMER(
CUSTOMER_NAME VARCHAR2(25), CUSTOMER_STREET VARCHAR2(25), CUSTOMER_CITY VARCHAR2(25),PRIMARY KEY(CUSTOMER_NAME));

CREATE TABLE LOAN(
LOAN_NUMBER NUMBER(10), BRANCH_NAME VARCHAR2(25), AMOUNT NUMBER(10,2),PRIMARY KEY(LOAN_NUMBER),
FOREIGN KEY(BRANCH_NAME) REFERENCES BRANCH (BRANCH_NAME) ON DELETE CASCADE);

CREATE TABLE BORROWER(
CUSTOMER_NAME VARCHAR2(25), LOAN_NUMBER NUMBER(10),
FOREIGN KEY(CUSTOMER_NAME) REFERENCES CUSTOMER (CUSTOMER_NAME) ON DELETE CASCADE,
FOREIGN KEY(LOAN_NUMBER) REFERENCES LOAN (LOAN_NUMBER) ON DELETE CASCADE);

